n = int(input())

power = 1
while power <= n:
    print(power, end=" ")
    power = power * 2




































# power = 1
# Начинаем с 1, потому что 1 = 2^0 — это тоже степень двойки.
# Это будет первая выводимая степень.

# while power <= n:
# Цикл while выполняется, пока текущее значение power
# не превышает n.
# Как только power станет больше n, цикл остановится.

# print(power, end=" ")
# Выводим текущее значение power.
# end=" " нужен, чтобы все числа выводились в одной строке
# и разделялись пробелами, как в примере из условия.

# power = power * 2
# Переходим к следующей степени двойки:
# 1 → 2 → 4 → 8 → 16 → 32 → ...
